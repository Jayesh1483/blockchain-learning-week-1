/*
Task: 
- Write a JavaScript function that takes two numbers as parameters and returns their sum.
- Test the function with different inputs and log the results to the console.
*/

/*
Notes:
1. We used concise arrow notation method to keep the function concise
2. We declared the function sumOfTwoNumberas constant
3. The benefit of using arrow notation is that we don't have to declare the keyword 'function' and 'return' explicitly   
*/

// Code
const sumOfTwoNumber = (number1, number2) => number1 + number2;
console.log(sumOfTwoNumber(5, 8));
console.log(sumOfTwoNumber(15, 9));
console.log(sumOfTwoNumber(39, 927));
